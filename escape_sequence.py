"""here we will learn about
\n-- new/next line
\t--tab
\\--backslash
\'-- inserts a single quote inside a single-quoted string
"""

#\n-----
print("hello everyone.\nhow are you?")     #\n is used to change the line.use it from where u want to change the line

#\t------
print("guddu 20")
print("guddu\t20")        #\t is used for tab or give more gap between two letters

#\\--------
print("yes\\no")       #\\ is used to print slash(\)  between two letters

#\'-------
print('This is python\'s class')            #if u want to use a single quote inside a single quoted sentence the before the single quote inside the sentence u should use this one bckslash(\')

#\"--------
print("hello its \"pythons\"class ")       # for using double quote inside a double quoted line we should use the slash before the inside double quote and after the inside double quote.
